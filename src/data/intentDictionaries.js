/**
 * Gets phrases for a specific intent and language
 * @param {string} intent - The intent type
 * @param {string} language - Language code (en, fr, de)
 * @returns {string[]} Array of phrases or empty array if not found
 */
export const getPhrasesByIntent = (intent, language = "en") => {
  const dictionaries = {
    greeting: greetingDictionary,
    farewell: farewellDictionary,
    gratitude: gratitudeDictionary,
    help: helpDictionary,
    confirmation: confirmationDictionary,
    negation: negationDictionary,
    question: questionDictionary,
    info: infoDictionary,
    comparison: comparisonDictionary,
    data: dataDictionary,
  };

  const dictionary = dictionaries[intent];
  if (!dictionary) {
    console.warn(`Intent dictionary not found: ${intent}`);
    return [];
  }

  return dictionary[language] || dictionary.en || [];
};

export default {
  greetingDictionary,
  farewellDictionary,
  gratitudeDictionary,
  helpDictionary,
  confirmationDictionary,
  negationDictionary,
  questionDictionary,
  infoDictionary,
  comparisonDictionary,
  dataDictionary,
  getPhrasesByIntent,
};
