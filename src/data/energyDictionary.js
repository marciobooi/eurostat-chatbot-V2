/**
 * Get related topics for a specific energy topic
 * @param {string} topic - Energy topic key
 * @param {string} language - Language code
 * @returns {string[]|null} Array of related topics or null if not found
 */
export const getRelatedTopics = (topic, language = "en") => {
  if (!topic) return null;

  const normalizedTopic = topic.toLowerCase();
  const langDict = energyDictionary[language] || energyDictionary.en;

  // Direct match
  if (langDict[normalizedTopic]?.relatedTopics) {
    return langDict[normalizedTopic].relatedTopics;
  }

  // Fuzzy match (checking if the topic is contained in any dictionary key)
  for (const [key, value] of Object.entries(langDict)) {
    if (key.includes(normalizedTopic) || normalizedTopic.includes(key)) {
      return value.relatedTopics;
    }
  }

  return null;
};

